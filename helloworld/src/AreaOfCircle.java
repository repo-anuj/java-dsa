public class AreaOfCircle {
    static void sum(double a, double b) {
        double sum = 3 + 4;
        System.out.println("The area of the circle is : " + sum);
    }

    static void sum3(double a, double b) {
        double sum = a * b * b;
        System.out.println("The area of the circle is : " + sum);
    }

    public static void main(String[] args) {
        sum3(3.14, 6.0);
    }
}
